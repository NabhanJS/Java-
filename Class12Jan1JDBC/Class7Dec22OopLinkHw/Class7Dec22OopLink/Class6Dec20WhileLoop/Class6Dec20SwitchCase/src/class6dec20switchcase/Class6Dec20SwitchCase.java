
package class6dec20switchcase;

import java.util.Scanner;

public class Class6Dec20SwitchCase {

    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        
        System.out.println("Please enter ur age");
        int age=sc.nextInt();
        
        switch(age){
            case 18:
                System.out.println("Your age is 18");
                break;
            case 20:
                 System.out.println("Your age is 20");
                 break;
                 case 22:
                      System.out.println("Your age is 22");
                      break;
                 default: 
                     System.out.println("Your age is unknown");
                     
                         
                         
        
        }
        
        
        
    
}
    
}

